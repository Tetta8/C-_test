#include <iostream>

using namespace std;

int main(){
    int x;
    int y;

    cout << "xとyを加減乗除します。" << endl;
    cout << "xとyの値";
    cin >> x >> y;

    cout << "x + y = " << x + y << "です。" << endl;
    cout << "x - y = " << x - y << "です。" << endl;
    cout << "x * y = " << x * y << "です。" << endl;
    cout << "x / y = " << x / y << "です。" << endl;
    cout << "x % y = " << x % y << "です。" << endl;
}